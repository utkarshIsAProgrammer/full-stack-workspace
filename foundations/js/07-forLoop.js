// for loop
for (let i = 1; i <= 5; i++) {
	console.log(i);
}

// nested for loop
for (let i = 1; i <= 5; i++) {
	console.log("--- OUTER LOOP ---", i);
	for (let j = 1; j <= 2; j++) {
		console.log("--- INNER LOOP ---", j);
	}
}

